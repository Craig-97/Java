package ducks;

public abstract class Duck {
	
	private String quack;
	private String swim;
	public String getQuack() {
		return this.quack;
	}
	public void setQuack(String quack) {
		this.quack = quack;
	}
	public String getSwim() {
		return this.swim;
	}
	public void setSwim(String swim) {
		this.swim = swim;
	}
	
	

}
